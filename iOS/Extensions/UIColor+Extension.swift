import UIKit

// MARK:- Color Extension......
extension UIColor {
    
    public static let successColor = UIColor(named: "SuccessColor")!
    public static let infoColor = UIColor(named: "InfoColor")!
    public static let errorColor = UIColor(named: "ErrorColor")!
    
    // Basic Color.....
    public static let orangeColor = UIColor(named: "OrangeColor")!
    public static let redColor = UIColor(named: "RedColor")!
    public static let lightGrayColor = UIColor(named: "LightGrayColor")!
    
    
    // gradiantColors
    static let firstColor = UIColor(named: "firstColor")!
    static let middleColor = UIColor(named: "middleColor")!
    static let lastColor = UIColor(named: "lastColor")!
    
    //
    static let whiteGrayColor = UIColor(named: "WhiteGrayColor")!
    
}
